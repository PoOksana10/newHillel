
let number1 = parseInt(prompt('Please, add first number'));
let number2 = parseInt(prompt("Please, add second number"));
if (number1 === number2) {
    alert("These are the same numbers");
} else if (number1 > number2) {
    alert("First number is bigger than second");
} else if (number2 > number1) {
    alert("Second number is bigger than first");
}




